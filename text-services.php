<div>
Нам часто задают следующие вопросы:<p></p>
<ol>
<li>Гарантируете ли вы, что ваши тексты поднимут продажи сайта?</li>
<li>Гарантируете ли вы, что страница с вашим текстом попадет в ТОП Яндекса?</li>
<li>Гарантируете ли вы, что посетители сайта будут читать и комментировать вашу статью?</li>
</ol>
<p>Ответим сразу на все три вопроса. Насколько эффективными будут написанные нами тексты, к сожалению, зависит не только от нас (в меньшей степени ― от нас). Попадет ли страница в ТОП выдачи поисковых систем, зависит не только от качества самого текста, но и от его оптимизации (правильно подобранных ключевых запросов, наличия метатегов, подзаголовков и пр.), а также от общего состояния сайта. Ведь если ваш сайт не оптимизирован для поисковых систем, то даже если статью напишет лучший эксперт в области, вряд ли она попадет в ТОП.</p>
<p>Мы хотим сразу проинформировать наших клиентов о том, что мы можем написать тексты на заказ, но без гарантий, что это даст какой-либо результат, т.е. нашей задачей является написание текстов высокого качества, но это будет в ваших руках лишь инструмент, которым нужно еще правильно воспользоваться.
</p>
